export function getDoctorById(id: string): Doctor | undefined {
  return mockDoctors.find((doctor) => doctor.id === id);
}

export function searchDoctors(query: string): Doctor[] {
  const lowerQuery = query.toLowerCase();
  return mockDoctors.filter(
    (doctor) =>
      doctor.city.toLowerCase().includes(lowerQuery) ||
      doctor.postalCode.toLowerCase().includes(lowerQuery) ||
      doctor.fullName.toLowerCase().includes(lowerQuery) ||
      doctor.clinicName.toLowerCase().includes(lowerQuery)
  );
}

export function filterDoctors(
  doctors: Doctor[],
  status?: DoctorStatus | "all"
): Doctor[] {
  if (!status || status === "all") return doctors;
  return doctors.filter((doctor) => doctor.acceptingStatus === status);
}
